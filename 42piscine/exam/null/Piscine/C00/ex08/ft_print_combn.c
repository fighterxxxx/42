#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_combn(int n)
{
	int i;

	i = 1;
	while (i < n)
	{
		ft_putchar();
		write(1, " ", 1);
		i++;
	}
}
